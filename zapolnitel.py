import sqlite3
import os

DB_PATH = "компьютерный_салон.db"

if os.path.exists(DB_PATH):
    os.remove(DB_PATH)

conn = sqlite3.connect(DB_PATH)
conn.execute("PRAGMA foreign_keys = ON")
cur = conn.cursor()

# ──────────────────────────────────────────────
# СПРАВОЧНЫЕ ТАБЛИЦЫ
# ──────────────────────────────────────────────

cur.executescript("""
-- 1. Категории товаров (справочная)
CREATE TABLE Категории_товаров (
    ид_категории    INTEGER PRIMARY KEY AUTOINCREMENT,
    название        TEXT    NOT NULL UNIQUE,
    описание        TEXT
);

-- 2. Должности (справочная)
CREATE TABLE Должности (
    ид_должности    INTEGER PRIMARY KEY AUTOINCREMENT,
    название        TEXT    NOT NULL UNIQUE,
    ставка_в_час    REAL    NOT NULL CHECK(ставка_в_час > 0)
);

-- 3. Поставщики (справочная)
CREATE TABLE Поставщики (
    ид_поставщика   INTEGER PRIMARY KEY AUTOINCREMENT,
    название        TEXT    NOT NULL UNIQUE,
    контактное_лицо TEXT    NOT NULL,
    телефон         TEXT    NOT NULL UNIQUE,
    email           TEXT    UNIQUE,
    адрес           TEXT,
    страна          TEXT    NOT NULL DEFAULT 'Россия'
);

-- 4. Услуги (справочная)
CREATE TABLE Услуги (
    ид_услуги       INTEGER PRIMARY KEY AUTOINCREMENT,
    название        TEXT    NOT NULL UNIQUE,
    описание        TEXT,
    базовая_цена    REAL    NOT NULL CHECK(базовая_цена >= 0),
    длительность_ч  REAL    NOT NULL CHECK(длительность_ч > 0)
);
""")

# ──────────────────────────────────────────────
# ОПЕРАЦИОННЫЕ ТАБЛИЦЫ
